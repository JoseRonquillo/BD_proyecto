    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        TiendaDAO td = new TiendaDAO();
        Boolean comprobacion = td.verificar_login(txtUsuario.getText(), txtContrasena.getText());
        if(comprobacion){
        dispose();
        String permisos = td.obtener_permisos(txtUsuario.getText(), txtContrasena.getText());
        Home hm = new Home(permisos);
        hm.setVisible(true);
        } else {
        JOptionPane.showMessageDialog(null,"Contrase√±a o Usuario incorrectos","Error",JOptionPane.INFORMATION_MESSAGE);
        txtUsuario.setText("");
        txtContrasena.setText("");
        }
    }//GEN-LAST:event_btEntrarActionPerformed

